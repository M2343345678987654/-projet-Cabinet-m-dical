    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        double total = 0;

    for (consultation c : ConsultationFr.listeconsultations) {
        try {
            total += Double.parseDouble(c.getPrix());
        } catch (NumberFormatException e) {
            // ignore si prix non valide
        }
    }

    javax.swing.JOptionPane.showMessageDialog(this,
            "Chiffre d'affaires total : " + total + " DH",
            "Chiffre d'affaires",
            javax.swing.JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) jTable1.getModel();

    // vider tableau
    model.setRowCount(0);

    // HashMap : date â†’ nombre de consultations
    java.util.HashMap<String, Integer> stats = new java.util.HashMap<>();

    for (consultation c : ConsultationFr.listeconsultations) {
        String date = c.getDate().trim();

        if (!stats.containsKey(date)) {
            stats.put(date, 1);
        } else {
            stats.put(date, stats.get(date) + 1);
        }
    }

    // remplir JTable
    for (String date : stats.keySet()) {
        model.addRow(new Object[]{date, stats.get(date)});
    }
    }//GEN-LAST:event_jButton3ActionPerformed

